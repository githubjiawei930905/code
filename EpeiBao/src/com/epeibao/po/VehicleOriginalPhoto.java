package com.epeibao.po;

import java.util.Date;


public class VehicleOriginalPhoto {
	private int vehicleOriginalPhotoId;
	private int vehicleOriginalPhotoBatchId;
	private String busNumber;
	private String phototype;
	private byte[] photoData;
	private Date upTime;
	private String cause;

	public int getVehicleOriginalPhotoId() {
		return vehicleOriginalPhotoId;
	}

	public void setVehicleOriginalPhotoId(int vehicleOriginalPhotoId) {
		this.vehicleOriginalPhotoId = vehicleOriginalPhotoId;
	}

	public int getVehicleOriginalPhotoBatchId() {
		return vehicleOriginalPhotoBatchId;
	}

	public void setVehicleOriginalPhotoBatchId(int vehicleOriginalPhotoBatchId) {
		this.vehicleOriginalPhotoBatchId = vehicleOriginalPhotoBatchId;
	}

	public String getBusNumber() {
		return busNumber;
	}

	public void setBusNumber(String busNumber) {
		this.busNumber = busNumber;
	}

	public String getPhototype() {
		return phototype;
	}

	public void setPhototype(String phototype) {
		this.phototype = phototype;
	}

	public byte[] getPhotoData() {
		return photoData;
	}

	public void setPhotoData(byte[] photoData) {
		this.photoData = photoData;
	}

	public Date getUpTime() {
		return upTime;
	}

	public void setUpTime(Date upTime) {
		this.upTime = upTime;
	}

	public String getCause() {
		return cause;
	}

	public void setCause(String cause) {
		this.cause = cause;
	}

}
