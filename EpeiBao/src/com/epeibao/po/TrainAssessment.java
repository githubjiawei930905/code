package com.epeibao.po;

/**
 * 培训考核实体类
 * @author Admin
 *
 */
public class TrainAssessment {
	private String comprehensiveAssessmentID;
	private int ifTrain;
	private String trainTime;
	private int ifAssessment;
	private String causeAnalysis;
	private int assessmentTimes;
	
	public String getComprehensiveAssessmentID() {
		return comprehensiveAssessmentID;
	}
	public void setComprehensiveAssessmentID(String comprehensiveAssessmentID) {
		this.comprehensiveAssessmentID = comprehensiveAssessmentID;
	}
	public int getIfTrain() {
		return ifTrain;
	}
	public void setIfTrain(int ifTrain) {
		this.ifTrain = ifTrain;
	}
	public String getTrainTime() {
		return trainTime;
	}
	public void setTrainTime(String trainTime) {
		this.trainTime = trainTime;
	}
	public int getIfAssessment() {
		return ifAssessment;
	}
	public void setIfAssessment(int ifAssessment) {
		this.ifAssessment = ifAssessment;
	}
	public String getCauseAnalysis() {
		return causeAnalysis;
	}
	public void setCauseAnalysis(String causeAnalysis) {
		this.causeAnalysis = causeAnalysis;
	}
	public int getAssessmentTimes() {
		return assessmentTimes;
	}
	public void setAssessmentTimes(int assessmentTimes) {
		this.assessmentTimes = assessmentTimes;
	}
	
}
