package com.epeibao.service;

import com.epeibao.po.Trainmaterial;

public interface TrainmaterialService {
	/**
	 * 添加每个批次的培训材料
	 * @param trainmaterial
	 * @throws Exception
	 */
	void saveTrainmaterial(Trainmaterial trainmaterial)throws Exception;
	/**
	 * 修改每个批次的培训材料
	 * @param trainmaterial
	 * @throws Exception
	 */
    void updateTrainmaterial(Trainmaterial trainmaterial)throws Exception;
    /**
     * 删除每个批次的培训材料
     * @param trainmaterialid
     * @throws Exception
     */
    void deleteTrainmaterial(String trainmaterialid)throws Exception;
    /**
     * 查找每个批次的培训材料
     * @param trainmaterialid
     * @return
     * @throws Exception
     */
    Trainmaterial findBytrainmaterialId(String trainmaterialid)throws Exception;
    /**
     * 查找每个公司的批次的培训材料
     * @param trainmaterialid
     * @return
     * @throws Exception
     */
    Trainmaterial findByOrganizationid(String organizationid)throws Exception;
}
